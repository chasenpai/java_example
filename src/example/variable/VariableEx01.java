package example.variable;

public class VariableEx01 {
	
	public static void main(String[] args) {
		
		int a; // 정수를 저장하기 위한 변수 a를 선언
		
		a = 5; // 변수 a에 5값을 저장
		
		System.out.println(a); //변수 a 값인 5가 출력된다
		
		int b = 10; // 변수를 선언과 동시에 값을 저장할 수 있다
		
		b = 15; // 변수 b에 15를 저장하면 기존의 값인 10은 지워짐
		
		System.out.println(b); // 변수 b의 값인 15가 출력된다
	}

}
